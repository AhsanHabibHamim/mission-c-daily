/*
Some double type variables name like num1, num2 etc. that will contain different double type number value
--> Write a program that print all the double data type number !
*/

#include <stdio.h>
int main()
{
    double num1 = 10.555555555555555;               // declare and initialize an double type variable
    double num2 = 20.2199999999915;                 // declare and initialize an double type variable
    printf("The Number 1 is %lf\n", num1);          // print the value of num1
    printf("The Number 2 is %lf\n", num2);          // print the value of num2
    printf("Number are %lf and %lf\n", num1, num2); // print the value of num1 and num2
}