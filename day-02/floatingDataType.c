/*
Some floating variables name like num1, num2 etc. that will contain different floating number value
--> Write a program that print all the floating number !
*/

#include <stdio.h>
int main()
{
    float num1 = 10.5;                            // declare and initialize an float variable
    float num2 = 20.215;                          // declare and initialize an float variable
    printf("The Number 1 is %f\n", num1);         // print the value of num1
    printf("The Number 2 is %f\n", num2);         // print the value of num2
    printf("Number are %f and %f\n", num1, num2); // print the value of num1 and num2
}