/*
Some intiger variables name like num1, num2 etc. that will contain different integer value 
--> Write a program that print all the integer number !
*/

#include <stdio.h>
int main()
{
    int num1 = 10; // declare and initialize an integer variable
    int num2 = 20; // declare and initialize an integer variable
    printf("The Number 1 is %d\n", num1); // print the value of num1
    printf("The Number 2 is %d\n", num2); // print the value of num2
    printf("Number are %d and %d\n", num1, num2); // print the value of num1 and num2
}